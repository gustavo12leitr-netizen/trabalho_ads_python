# Exercício 7: Cálculo de Média Ponderada
# Valores de entrada devem ser inteiros conforme o enunciado
a = int(input("Digite o valor de A: "))
b = int(input("Digite o valor de B: "))
c = int(input("Digite o valor de C: "))
d = int(input("Digite o valor de D: "))

# Cálculo da média seguindo a fórmula fornecida
media = (a * 3 + b * 4 + c * 2 + d * 5) / 14

print(f"\nA média ponderada calculada é: {media:.2f}")
